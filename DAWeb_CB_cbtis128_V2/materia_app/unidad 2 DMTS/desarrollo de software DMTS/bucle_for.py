#Bucle for para integrar (repetir) una secuencia de codigos 
#interar sobre una lista 
#Utiñiza operador de pertenencia "in"

frutas =["manzanas","pera","platano",]

#Iiniciamos bucle for con sus sintaxis correspondiente 
#for variable secuencia 
#variable puede ser cualquiera a,b,c.....z
#operador de asignacion in,nos indica ña secuncua de dato 
#que se estara interando (repitendo)
#secuencia:la informcion que se utuliza en el for 
for i in frutas: 
    print("La fruta es:",i,"!!!!") 

#funcion range para genrar una secuencia de iteracion 
#(repeticiones )
#Inicuamos bucle for con su sintaxis correspondiente 
#for <variable> in <range(star, stop-1)>
for i in range(1,11):
    print("Numero:",i,"!!!!")

    
